const month = 3
// const month = "feb"


switch (month) {
    case 1:
        console.log('January');
        break;
    case 2:
        console.log('February');
        break;
    case 3:
        console.log('March');
        break;
    case 4:
        console.log('April');
        break;
    default:
        console.log('default case');
        break;
}

// break; is important, it is  used to terminate the execution of the switch statement after a matching case has been found
// so it doesnot print multiple case even without matching

